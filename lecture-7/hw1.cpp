// Homework1: (6 & 10), (6 | 10), (6 ^ 10)
// (10 << 2)
// (10 >> 1)

#include <iostream>
using namespace std;

int main() {
    
    cout << (6 & 10) << endl;   // 2
    cout << (6 | 10) << endl;   // 14
    cout << (6 ^ 10) << endl;   // 12
    
    cout << (10 << 2) << endl;   // 40
    cout << (10 >> 1) << endl;   // 5

    return 0;
}
